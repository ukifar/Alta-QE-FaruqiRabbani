class Operasi extends Input {

    public float penjumlahan() {
        return (input1 + input2);
    }
    public float pengurangan() {
        return (input1 - input2);
    }
    public float perkalian() {
        return (input1 * input2);
    }
    public float pembagian() {
        return input1 / input2;
    }
}
